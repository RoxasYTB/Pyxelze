    public static void SetLanguage(string lang)
    {
        if (_strings.ContainsKey(lang))
            _currentLang = lang;
    }

    private static void DetectSystemLanguage()
    {
        var culture = CultureInfo.CurrentUICulture.TwoLetterISOLanguageName;
        if (_strings.ContainsKey(culture))
            _currentLang = culture;
        else
            _currentLang = "en";
    }

    public static string Get(string key)
    {
        if (_strings.TryGetValue(_currentLang, out var dict) && dict.TryGetValue(key, out var val))
            return val;
        if (_strings.TryGetValue("en", out var enDict) && enDict.TryGetValue(key, out var enVal))
            return enVal;
        return key;
    }

    public static string Get(string key, params object[] args)
    {
        var template = Get(key);
        try { return string.Format(template, args); }
        catch { return template; }
    }

    private static void Reg(string lang, Dictionary<string, string> entries)
    {
        _strings[lang] = entries;
    }

